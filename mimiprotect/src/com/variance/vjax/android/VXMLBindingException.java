package com.variance.vjax.android;

public class VXMLBindingException extends Exception {

	private static final long serialVersionUID = 9214110324428192350L;

	public VXMLBindingException() {
		super();
	}

	public VXMLBindingException(String detailMessage, Throwable throwable) {
		super(detailMessage, throwable);
	}

	public VXMLBindingException(String detailMessage) {
		super(detailMessage);
	}

	public VXMLBindingException(Throwable throwable) {
		super(throwable);
	}

}
